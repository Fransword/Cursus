void	ft_alpha(int alpha)
{
	if (alpha >= 'a' && alpha <= 'z' || alpha >= 'A' && alpha <= 'Z')
	{
		write (1, &alpha, 1);
	}
	else
	{
		write(1, "no", 1);
	}
}
