char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	len;
	size_t	i;
	char	*str;

	str = (char *)malloc(sizeof(char) * (len + 1));
	i = 0;
	if (!s || !f)
		return (0);
	len = ft_strlen(s);
	if (!str)
		return (0);
	while (i < len)
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
